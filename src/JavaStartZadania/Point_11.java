package JavaStartZadania;

public class Point_11 {
    private int x;
    private int y;

    public Point_11() {
    }

    public Point_11(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
