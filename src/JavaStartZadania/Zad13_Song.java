package JavaStartZadania;

public class Zad13_Song {
    String name;
    int time;

    public Zad13_Song(String name, int time){
        this.name=name;
        this.time=time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
