package JavaStartZadania;

public class Person_16 {
    private int personWeight;
    private int personID;
    private int floorAim;

    public Person_16(int personWeight, int personID, int floorAim){
        this.personWeight = personWeight;
        this.personID = personID;
        this.floorAim = floorAim;
    }

    public int getFloorAim() {
        return floorAim;
    }

    public void setFloorAim(int floorAim) {
        this.floorAim = floorAim;
    }

    public int getPersonWeight() {
        return personWeight;
    }

    public void setPersonWeight(int personWeight) {
        this.personWeight = personWeight;
    }

    public int getPersonID() {
        return personID;
    }

    public void setPersonID(int personID) {
        this.personID = personID;
    }

}
